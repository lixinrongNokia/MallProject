public class DepthResult {

    private String rujianju;
    private String fenmandu;
    private String rufanwei;
    private String ruwaixing;
    private String rutoufanxiang;

    public String getRujianju() {
        return rujianju;
    }

    public void setRujianju(String rujianju) {
        this.rujianju = rujianju;
    }

    public String getFenmandu() {
        return fenmandu;
    }

    public void setFenmandu(String fenmandu) {
        this.fenmandu = fenmandu;
    }

    public String getRufanwei() {
        return rufanwei;
    }

    public void setRufanwei(String rufanwei) {
        this.rufanwei = rufanwei;
    }

    public String getRuwaixing() {
        return ruwaixing;
    }

    public void setRuwaixing(String ruwaixing) {
        this.ruwaixing = ruwaixing;
    }

    public String getRutoufanxiang() {
        return rutoufanxiang;
    }

    public void setRutoufanxiang(String rutoufanxiang) {
        this.rutoufanxiang = rutoufanxiang;
    }

    @Override
    public String toString() {
        return "乳间距"+rujianju+",丰满度"+fenmandu+",乳房位"+rufanwei+",乳外形"+ruwaixing+",乳头方向"+rutoufanxiang;
    }
}
