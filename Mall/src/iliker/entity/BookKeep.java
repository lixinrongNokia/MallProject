package iliker.entity;

public class BookKeep {
    private int spendID;
    private String spendTime;
    private String spendDESC;
    private String spendAmount;

    public int getSpendID() {
        return spendID;
    }

    public void setSpendID(int spendID) {
        this.spendID = spendID;
    }

    public String getSpendTime() {
        return spendTime;
    }

    public void setSpendTime(String spendTime) {
        this.spendTime = spendTime;
    }

    public String getSpendDESC() {
        return spendDESC;
    }

    public void setSpendDESC(String spendDESC) {
        this.spendDESC = spendDESC;
    }

    public String getSpendAmount() {
        return spendAmount;
    }

    public void setSpendAmount(String spendAmount) {
        this.spendAmount = spendAmount;
    }
}
